export const convertNationalityCode = (code: string): string => {
  if (!code) return "";
  
  // Nettoyer le code (enlever les espaces, mettre en majuscules)
  const cleanCode = code.trim().toUpperCase();
  
  // Retourner le nom complet si trouvé, sinon retourner le code original
  return nationalityCodeMapping[cleanCode] || cleanCode;
};
